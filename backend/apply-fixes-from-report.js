async function fixHTTP500Errors(errors) {
  let fixed = 0;
  let failed = 0;
  let skipped = 0;

  for (const error of errors) {
    const httpErrors = error.context?.http_errors || [];

    for (const httpError of httpErrors) {
      if (httpError.status === 500) {
        console.log(`   üîç HTTP 500: ${httpError.url}`);

        // Analizar qu√© endpoint fall√≥
        if (httpError.url.includes('/api/audit/status')) {
          console.log('   ‚ÑπÔ∏è  Error en /api/audit/status');
          console.log('   üí° Fix sugerido: Verificar autenticaci√≥n en auditorRoutes.js');
          skipped++;
        } else {
          console.log('   ‚ÑπÔ∏è  Error desconocido, requiere an√°lisis manual');
          skipped++;
        }
      }
    }
  }

  return { fixed, failed, skipped };
}

